# 5.7 – Fruta favorita: Faça uma lista de suas frutas favoritas e, então, escreva uma
# série de instruções if independentes que verifiquem se determinadas frutas estão em sua lista.
# • Crie uma lista com suas três frutas favoritas e chame-a de favorite_fruits.
# • Escreva cinco instruções if. Cada instrução deve verificar se uma determinada
# fruta está em sua lista. Se estiver, o bloco if deverá exibir uma frase, por exemplo, Você realmente gosta de bananas!
favorite_fruts = ['maça', 'banana', 'morango']

if 'maça' in favorite_fruts:
    print("Você realmente gosta de maça")
if 'abacaxi' in favorite_fruts:
    print("Você realmente gosta de abacaxi")
if 'morango' in favorite_fruts:
    print("Você realmente gosta de morango")
if 'manga' in favorite_fruts:
    print("Você realmente gosta de manga")
if 'pera' in favorite_fruts:
    print("Você realmente gosta de pera")

